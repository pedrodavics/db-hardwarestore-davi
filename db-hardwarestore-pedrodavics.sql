-- Função que calcula total de compras de um cliente
CREATE FUNCTION VALORTOTAL (@ID_CLIENTE INT)
RETURNS DECIMAL(10, 2)
AS
BEGIN
     DECLARE @VALOR_TOTAL DECIMAL(10, 2)

     SELECT @VALOR_TOTAL = SUM(B.VALOR * A.QTD_PRODUTO)
     FROM PEDIDOS AS A
     INNER JOIN PRODUTOS AS B ON A.ID_PRODUTO = B.ID_PRODUTO
     WHERE A.ID_CLIENTE = @ID_CLIENTE

     RETURN @VALOR_TOTAL
END

