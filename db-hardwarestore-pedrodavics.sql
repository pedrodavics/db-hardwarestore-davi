-- Valor total de vendas por mês
SELECT
    YEAR(DATA_PEDIDO) AS ANO,
    MONTH(DATA_PEDIDO) AS MES,
    SUM(QTD_PRODUTO * VALOR) AS VALOR_TOTAL_VENDAS
FROM
    PEDIDOS
    INNER JOIN PRODUTOS ON PEDIDOS.ID_PRODUTO = PRODUTOS.ID_PRODUTO
GROUP BY
    YEAR(DATA_PEDIDO),
    MONTH(DATA_PEDIDO)
ORDER BY
    ANO, MES

-- Produtos mais vendidos
SELECT
    P.PRODUTO,
    SUM(PEDIDOS.QTD_PRODUTO) AS QUANTIDADE_VENDIDA
FROM
    PEDIDOS
    INNER JOIN PRODUTOS P ON PEDIDOS.ID_PRODUTO = P.ID_PRODUTO
GROUP BY
    P.PRODUTO
ORDER BY
    QUANTIDADE_VENDIDA DESC


-- Valor total de vendas por gênero
SELECT
    B.GENERO,
    SUM(A.QTD_PRODUTO * C.VALOR) AS VALOR_TOTAL_VENDAS
FROM
    PEDIDOS A
    INNER JOIN CLIENTES B ON A.ID_CLIENTE = B.ID_CLIENTE
    INNER JOIN PRODUTOS C ON A.ID_PRODUTO = C.ID_PRODUTO
GROUP BY 
    B.GENERO

-- vendas por faixa etaria
SELECT
    CASE
        WHEN DATEDIFF(YEAR, B.DATA_NASCIMENTO, GETDATE()) BETWEEN 18 AND 25 THEN '18-25'
        WHEN DATEDIFF(YEAR, B.DATA_NASCIMENTO, GETDATE()) BETWEEN 26 AND 35 THEN '26-35'
        WHEN DATEDIFF(YEAR, B.DATA_NASCIMENTO, GETDATE()) BETWEEN 36 AND 45 THEN '36-45'
        ELSE 'Acima de 45'
    END AS FAIXA_ETARIA,
    SUM(A.QTD_PRODUTO * C.VALOR) AS VALOR_TOTAL_VENDAS
FROM
    PEDIDOS A
    INNER JOIN CLIENTES B ON A.ID_CLIENTE = B.ID_CLIENTE
    INNER JOIN PRODUTOS C ON A.ID_PRODUTO = C.ID_PRODUTO
GROUP BY
    CASE
        WHEN DATEDIFF(YEAR, B.DATA_NASCIMENTO, GETDATE()) BETWEEN 18 AND 25 THEN '18-25'
        WHEN DATEDIFF(YEAR, B.DATA_NASCIMENTO, GETDATE()) BETWEEN 26 AND 35 THEN '26-35'
        WHEN DATEDIFF(YEAR, B.DATA_NASCIMENTO, GETDATE()) BETWEEN 36 AND 45 THEN '36-45'
        ELSE 'Acima de 45'
    END


-- Vendas por região
SELECT
    C.CIDADE AS REGIAO,
    SUM(P.QTD_PRODUTO * PR.VALOR) AS VALOR_TOTAL_VENDAS
FROM
    PEDIDOS P
    INNER JOIN CLIENTES CL ON P.ID_CLIENTE = CL.ID_CLIENTE
    INNER JOIN PRODUTOS PR ON P.ID_PRODUTO = PR.ID_PRODUTO
    INNER JOIN ENTREGAS E ON P.ID_ENTREGA = E.ID_ENTREGA
    INNER JOIN FORNECEDORES F ON P.ID_FORNECEDOR = F.ID_FORNECEDOR
    INNER JOIN CLIENTES C ON E.ID_CLIENTE = C.ID_CLIENTE
GROUP BY
    C.CIDADE
ORDER BY
    VALOR_TOTAL_VENDAS DESC


