-- tabela clientes
CREATE TRIGGER CLIENTES_AUDIT
ON CLIENTES
AFTER INSERT, UPDATE, DELETE
AS
BEGIN
    DECLARE @Operacao NVARCHAR(10)
    DECLARE @Usuario NVARCHAR(100)
    
    SET @Usuario = SUSER_SNAME()

    IF EXISTS (SELECT * FROM inserted)
    BEGIN
        IF EXISTS (SELECT * FROM deleted)
            SET @Operacao = 'UPDATE'
        ELSE
            SET @Operacao = 'INSERT'

        INSERT INTO AUDITORIA_2 (TABELA, OPERACAO, ANTES, DEPOIS, DATAHORA, USUARIO)
        SELECT 'CLIENTES', @Operacao, 
               (SELECT * FROM deleted FOR XML PATH('')), 
               (SELECT * FROM inserted FOR XML PATH('')), 
               GETDATE(),
               @Usuario
    END
    ELSE IF EXISTS (SELECT * FROM deleted)
    BEGIN
        SET @Operacao = 'DELETE'

        INSERT INTO AUDITORIA_2 (TABELA, OPERACAO, ANTES, DEPOIS, DATAHORA, USUARIO)
        SELECT 'CLIENTES', @Operacao, 
               (SELECT * FROM deleted FOR XML PATH('')), 
               NULL, 
               GETDATE(),
               @Usuario
    END
END

