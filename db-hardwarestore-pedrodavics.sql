CREATE DATABASE LOJA_CONSTRUCAO
USE LOJA_CONSTRUCAO

-- criação das tabelas

CREATE TABLE CLIENTES (
ID_CLIENTE INT PRIMARY KEY NOT NULL, 
NOME VARCHAR(100), 
CPF CHAR(14) NOT NULL,
GENERO VARCHAR(20),
DATA_NASCIMENTO DATE, 
EMAIL VARCHAR(100),
TELEFONE VARCHAR(20),
ENDERECO VARCHAR(100),
CIDADE VARCHAR(50),
ESTADO VARCHAR(20),
)

CREATE TABLE FORNECEDORES (
ID_FORNECEDOR INT PRIMARY KEY NOT NULL,
NOME VARCHAR(100),
CNPJ CHAR(18) NOT NULL,
EMAIL VARCHAR(100),
TELEFONE VARCHAR(20),
ENDERECO VARCHAR(200),
CIDADE VARCHAR(50),
ESTADO VARCHAR(20),
)

CREATE TABLE PRODUTOS (
ID_PRODUTO INT PRIMARY KEY NOT NULL,
PRODUTO VARCHAR(50),
ID_FORNECEDOR INT NOT NULL,
PESO DECIMAL(10,2),
TAMANHO VARCHAR(20),
VALOR DECIMAL(12,2),
FOREIGN KEY (ID_FORNECEDOR) REFERENCES FORNECEDORES(ID_FORNECEDOR)
)

CREATE TABLE MOTORISTAS (
ID_MOTORISTA INT PRIMARY KEY not null,
NOME VARCHAR(100),
TELEFONE VARCHAR(20),
CPF CHAR(14),
EMPRESA_ENTREGA VARCHAR(100),
TIPO_VEICULO VARCHAR(50),
)

CREATE TABLE ENTREGAS (
ID_ENTREGA INT PRIMARY KEY NOT NULL,
DATA_ENTREGA DATE,
ID_CLIENTE INT NOT NULL,
ENDERECO_CLIENTE VARCHAR(100),
ID_FORNECEDOR INT NOT NULL,
ENDERECO_FORNECEDOR VARCHAR(100),
PRODUTO VARCHAR(20),
ID_MOTORISTA INT NOT NULL,
TIPO_VEICULO VARCHAR(50),
ID_PEDIDO INT NOT NULL,
FOREIGN KEY (ID_CLIENTE) REFERENCES CLIENTES(ID_CLIENTE),
FOREIGN KEY (ID_FORNECEDOR) REFERENCES FORNECEDORES(ID_FORNECEDOR),
FOREIGN KEY (ID_MOTORISTA) REFERENCES MOTORISTAS(ID_MOTORISTA)
)


CREATE TABLE PEDIDOS (
ID_PEDIDO INT PRIMARY KEY NOT NULL,
DATA_PEDIDO DATE,
ID_CLIENTE INT NOT NULL,
ID_FORNECEDOR INT NOT NULL,
ID_PRODUTO INT NOT NULL,
QTD_PRODUTO INT NOT NULL,
ID_ENTREGA INT NOT NULL,
FOREIGN KEY (ID_CLIENTE) REFERENCES CLIENTES(ID_CLIENTE),
FOREIGN KEY (ID_FORNECEDOR) REFERENCES FORNECEDORES(ID_FORNECEDOR),
FOREIGN KEY (ID_PRODUTO) REFERENCES PRODUTOS(ID_PRODUTO)
)

