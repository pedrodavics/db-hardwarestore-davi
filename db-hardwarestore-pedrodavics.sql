-- Função para calcular a idade do cliente
CREATE FUNCTION IDADECLIENTE (@ID_CLIENTE INT)
RETURNS INT
AS
BEGIN
     DECLARE @DATA_NASCIMENTO DATE
     DECLARE @IDADE INT

     SELECT @DATA_NASCIMENTO = DATA_NASCIMENTO FROM CLIENTES WHERE ID_CLIENTE = @ID_CLIENTE

     SET @IDADE = DATEDIFF(YEAR, @DATA_NASCIMENTO, GETDATE())

     RETURN @IDADE
END

