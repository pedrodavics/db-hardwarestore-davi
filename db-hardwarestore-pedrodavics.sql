-- procedure de update de valor na tabela produtos utilizando comandos TCL
CREATE PROCEDURE ATUALIZAR_VALOR_PRODUTO (
     @ID_PRODUTO INT,
     @PRODUTO VARCHAR(50), 
     @ID_FORNECEDOR INT,
     @PESO DECIMAL(10,2),
     @TAMANHO VARCHAR(20),
     @VALOR DECIMAL(12,2) )
AS
BEGIN
    BEGIN TRANSACTION

    BEGIN TRY
        IF EXISTS (SELECT 1 FROM PRODUTOS WHERE ID_PRODUTO = @ID_PRODUTO)
        BEGIN
            UPDATE PRODUTOS
            SET VALOR = @VALOR
            WHERE ID_PRODUTO = @ID_PRODUTO

            COMMIT
            PRINT 'Valor do produto atualizado com sucesso.'
        END
        ELSE
        BEGIN
            ROLLBACK
            PRINT 'Erro ao atualizar o valor do produto. Produto não encontrado.'
        END
    END TRY
    BEGIN CATCH
        ROLLBACK
        PRINT ERROR_MESSAGE()
    END CATCH
END
	
