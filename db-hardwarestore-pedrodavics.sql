-- view com o valor total e resumo de cada pedido
CREATE VIEW RESUMO_PEDIDO AS
SELECT  ID_PEDIDO AS 'ID DO PEDIDO',
        (CONCAT(B.ID_FORNECEDOR, ' ', B.NOME)) AS 'ID E NOME FORNECEDOR',       	  
	    (CONCAT(C.ID_CLIENTE, ' ', C.NOME)) AS 'ID E NOME CLIENTE',
		(CONCAT(D.PRODUTO, ' ', A.QTD_PRODUTO)) AS 'PRODUTO/UNIDADES',
		A.QTD_PRODUTO * D.VALOR AS 'VALOR TOTAL'
FROM PEDIDOS AS A
JOIN FORNECEDORES AS B
     ON A.ID_FORNECEDOR = B.ID_FORNECEDOR
JOIN CLIENTES AS C
     ON A.ID_CLIENTE = C.ID_CLIENTE
JOIN PRODUTOS AS D
     ON A.ID_PRODUTO = D.ID_PRODUTO

SELECT * FROM RESUMO_PEDIDO


-- view com o resumo das entregas, incluindo peso total dos itens de cada pedido 
CREATE VIEW RESUMO_ENTREGA AS
SELECT A.ID_PEDIDO AS 'ID PEDIDO',
       C.NOME AS FORNECEDOR,
       B.NOME AS CLIENTE,
	   D.PRODUTO AS PRODUTO,
	   A.QTD_PRODUTO AS UNIDADES,
	   A.QTD_PRODUTO * D.PESO AS 'PESO TOTAL',
	   F.NOME AS MOTORISTA,
	   F.TIPO_VEICULO AS VEICULO 
FROM PEDIDOS AS A
JOIN CLIENTES AS B
     ON A.ID_CLIENTE = B.ID_CLIENTE
JOIN FORNECEDORES AS C
     ON A.ID_FORNECEDOR = C.ID_FORNECEDOR
JOIN PRODUTOS AS D
     ON A.ID_PRODUTO = D.ID_PRODUTO
JOIN ENTREGAS AS E
     ON A.ID_ENTREGA = E.ID_ENTREGA
JOIN MOTORISTAS AS F
     ON E.ID_MOTORISTA = F.ID_MOTORISTA

SELECT * FROM RESUMO_ENTREGA

